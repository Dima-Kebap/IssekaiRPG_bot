    def sell(self, user):
        user.money += self.price
        self.count -= 1
        price = str(self.price)
        name = self.name
        if user.items[self.name].count == 0:
            user.items.pop(self.name)
        return "–¢—ã –ø—Ä–æ–¥–∞–ª {0} –∏ –ø–æ–ª—É—á–∏–ª {1} üíµ".format(name, price)

